function insertionSort (arr) {
  for(let i=0; i < arr.length; i++){
    const curr = arr[i];
    let j =  i - 1;
    while (j >= 0 && arr[j+1] > curr) {
        arr[j+1] = arr[j]
        j--
    }
    arr[j+1] = curr
  }
}